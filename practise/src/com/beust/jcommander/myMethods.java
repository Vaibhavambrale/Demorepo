package com.beust.jcommander;

public class myMethods {
	
	public static void main(String args[]) {
		int marks[]= {1,2,3,4,};
		for(int value:marks) {
			System.out.print(value+" ");
		}
		System.out.println();
		for (int i=0;i<marks.length;i++) {
			System.out.print(marks[i]);
		}
		System.out.println(marks.length);
	}
	
	
	
	
}